// 1. منع Rank Math والووردبريس من عمل إعادة توجيه للينكات البورتفوليو
add_filter('redirect_canonical', 'stop_portfolio_redirect', 10, 2);
function stop_portfolio_redirect($redirect_url, $requested_url) {
    if (strpos($_SERVER['REQUEST_URI'], '/portfolio/') !== false) {
        return false; 
    }
    return $redirect_url;
}

// 2. اعتراض الـ 404 وإجبار السيرفر يعرض صفحة البورتفوليو
add_action('template_redirect', 'force_portfolio_page_load', 1);
function force_portfolio_page_load() {
    $url = $_SERVER['REQUEST_URI'];
    if (strpos($url, '/portfolio/') !== false && $url !== '/portfolio/') {
        global $wp_query;
        $wp_query->set_404(false);
        status_header(200);
        $page = get_page_by_path('portfolio');
        if ($page) {
            $wp_query->posts = array($page);
            $wp_query->post_count = 1;
            $wp_query->is_404 = false;
            $wp_query->is_page = true;
            $wp_query->is_singular = true;
            $wp_query->queried_object = $page;
            $wp_query->queried_object_id = $page->ID;
        }
    }
}

// 3. إصلاح عنوان التابة (Title) فوق عشان ما يكتبش Not Found ويظهر بشكل احترافي
add_filter('pre_get_document_title', 'fix_portfolio_dynamic_title', 999);
add_filter('rank_math/frontend/title', 'fix_portfolio_dynamic_title', 999);
function fix_portfolio_dynamic_title($title) {
    $url = $_SERVER['REQUEST_URI'];
    if (strpos($url, '/portfolio/') !== false && $url !== '/portfolio/') {
        // استخراج اسم التابة من الرابط 
        $path_parts = explode('/', trim($url, '/'));
        $slug = end($path_parts);
        
        // تحويل السلاج لاسم مقروء (مثلاً 2d-explainer-videos إلى 2d Explainer Videos)
        $tab_name = ucwords(str_replace('-', ' ', $slug));
        
        // العنوان الجديد اللي هيظهر فوق
        return $tab_name . ' | Portfolio';
    }
    return $title;
}

// 4. منع Rank Math من وضع علامة 404 في أكواد الـ SEO عشان جوجل يأرشف اللينكات صح
add_filter('rank_math/frontend/robots', 'fix_portfolio_seo_robots', 999);
function fix_portfolio_seo_robots($robots) {
    $url = $_SERVER['REQUEST_URI'];
    if (strpos($url, '/portfolio/') !== false && $url !== '/portfolio/') {
        $robots['index'] = 'index';
        $robots['follow'] = 'follow';
    }
    return $robots;
}
/**
 * تحميل Custom CSS & JS للهيدر والفوتر
 */
function my_custom_header_footer_assets() {
    // التأكد من تحميل jQuery أولاً
    wp_enqueue_script('jquery');
    
    $css_file = get_template_directory() . '/assets/css/custom-style.css';
    $js_file = get_template_directory() . '/assets/js/custom-menu.js';
    
    // تحميل CSS
    if (file_exists($css_file)) {
        wp_enqueue_style(
            'my-custom-header-footer',
            get_template_directory_uri() . '/assets/css/custom-style.css',
            array(),
            filemtime($css_file)
        );
    }
    
    // تحميل JS
    if (file_exists($js_file)) {
        wp_enqueue_script(
            'my-custom-menu-js',
            get_template_directory_uri() . '/assets/js/custom-menu.js',
            array('jquery'), // jQuery كاعتماد
            filemtime($js_file),
            true // تحميل في footer
        );
    }
}
add_action('wp_enqueue_scripts', 'my_custom_header_footer_assets', 999);

